public class Guinniepig {
    private String name;
    private String breed;
    private int foodInGrams;

    public Guinniepig(String name, String breed, int foodInGrams) {
        this.name = name;
        this.breed = breed;
        this.foodInGrams = foodInGrams;
    }

    public String getName() {
        return name;
    }

    public String getBreed() {
        return breed;
    }

    public int getFoodInGrams() {
        return foodInGrams;
    }
}
